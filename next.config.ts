import type { NextConfig } from "next";

// Static security headers applied to every response by the framework layer.
// Content-Security-Policy is intentionally absent here — it is set dynamically
// in middleware.ts with a per-request nonce (removes 'unsafe-inline' for scripts).
const securityHeaders = [
  { key: "X-Frame-Options", value: "DENY" },
  { key: "X-Content-Type-Options", value: "nosniff" },
  { key: "Referrer-Policy", value: "no-referrer" },
  {
    key: "Permissions-Policy",
    value: "camera=(), microphone=(), geolocation=()",
  },
];

const nextConfig: NextConfig = {
  output: "standalone",
  poweredByHeader: false,
  async headers() {
    return [{ source: "/:path*", headers: securityHeaders }];
  },
};

export default nextConfig;
